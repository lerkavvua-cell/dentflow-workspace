export function formatTime(value: number) {
  return new Date(value).toLocaleString([], {
    day: '2-digit',
    month: '2-digit',
    hour: '2-digit',
    minute: '2-digit'
  });
}

export function normalizePatientId(workspace: WorkspaceKey, name: string) {
  const safe = name.trim().toLowerCase().replace(/[^\wа-яёіїєґçğıöşüآ-ی]+/gi, '-').replace(/^-|-$/g, '');
  return `${workspace}-${safe || Date.now()}`;
}
